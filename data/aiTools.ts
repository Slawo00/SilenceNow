export const getToolById = (id: string): AIToolExtended | undefined => {
  return aiToolsCatalog.find(tool => tool.id === id);
};

export const getToolByName = (name: string): AIToolExtended | undefined => {
  return aiToolsCatalog.find(tool => tool.name.toLowerCase() === name.toLowerCase());
};

export const getToolsByCategory = (category: string): AIToolExtended[] => {
  return aiToolsCatalog.filter(tool => tool.category === category);
};

export const getToolsByGoal = (goalId: string): AIToolExtended[] => {
  return aiToolsCatalog.filter(tool => tool.recommendedGoals.includes(goalId));
};

export const getAllCategories = (): string[] => {
  const categories = new Set<string>();
  aiToolsCatalog.forEach(tool => categories.add(tool.category));
  return Array.from(categories).sort();
};

export const getToolCount = (): number => {
  return aiToolsCatalog.length;
};
